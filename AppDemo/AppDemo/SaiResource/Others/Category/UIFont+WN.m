#import "UIFont+WN.h"

@implementation UIFont (WN)
+ (UIFont *)qk_PingFangSCRegularFontwithSize:(CGFloat )size{
    return  [UIFont fontWithName:@"PingFangSC-Light" size:size];
}

+ (UIFont *)qk_PingFangSCRegularBoldFontwithSize:(CGFloat )size{
    return  [UIFont fontWithName:@"Helvetica-Bold" size:size];
}
@end
